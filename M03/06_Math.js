//********** OBJETO Math **********

// --> Potenciar:

Math.pow(2,3);   // 2³ ==> 8


// --> Redondear a entero más próximo:

Math.round(0.49); // ==> 0

Math.round(0.88) // ==> 1


// --> Redondear a entero inmediatamente anterior:

Math.floor(5.93); // ==> 5


// --> Redondear a entero inmediatamente siguiente:

Math.ceil(3.27); // ==> 4


// --> Valor máximo:

Math.max(1,2,3,4,5); // ==> 5


// --> Valor mínimo:

Math.min(1,2,3,4,5); // ==> 1


// --> Número aleatorio entre 0 y 1:

Math.random(); // ==> 0.4514404421574827


